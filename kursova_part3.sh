#!/bin/bash

# Перевірка наявності двох параметрів
if [ "$#" -ne 2 ]; then
    echo "Введіть два числові параметри."
    exit 1
fi

# Перевірка, чи є параметри числами
if ! [[ "$1" =~ ^[0-9]+$ ]] || ! [[ "$2" =~ ^[0-9]+$ ]]; then
    echo "Параметри мають бути числами."
    exit 1
fi

# Збереження параметрів у змінних
num1=$1
num2=$2

# Збереження назви скрипта
script_name=$(basename "$0")

# Порівняння параметрів
if [ "$num1" -gt "$num2" ]; then
    echo "Перший параметр ($num1) більше за другий ($num2)."
    echo "Список псевдонімів (alias):"
    alias
else
    echo "Перший параметр ($num1) не більше за другий ($num2)."
    echo "Розмір файлу скрипта '$script_name':"
    stat --format="%s" "$script_name"
fi
